#Change Dictionary

thisdict = {
    "brand": "Ford",
"model": "Mustang",
"year": 1964
}
thisdict["year"] = 2018
#change the "year" to 2018


thisdict = {
    "brand": "Ford",
"model": "Mustang",
"year": 1964
}
thisdict.update({"year":  2018})
#update the "year" of the car by using update() method




